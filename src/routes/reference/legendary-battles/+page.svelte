	<section>
		<p>Occasionally, a DM might want to throw an extra difficult boss at their players. A giant Onix terrorizing the town? A monstrous Huntail gobbling up fishing boats in the harbor? A Legendary Pokémon at the end of a long journey? Here are some actions and abilities you can give to an enemy to boost their deadliness in battle.</p>
	</section>
	<section>
		<h2>Legendary Abilities</h2>
		<p>It is recommended for DMs to award extra experience for defeating creatures with one or more of these skills.</p>

		<h3>Legendary Grit (X/Day)</h3>
		<p>If this Pokémon fails a saving throw, it can choose to succeed instead.</p>

		<h3>Legendary Resilience</h3>
		<p>This Pokémon may sacrifice its action to remove a negative status effect from itself.</p>

		<h3>Legendary Resistance (X/Day)</h3>
		<p>When this Pokémon takes damage from a move, it can choose to halve the total.</p>

		<h3>Legendary Attack I</h3>
		<p>This Pokémon can sacrifice its action now to make two attacks on its next turn. Or, it may make two attacks now and sacrifice its next action.</p>

		<h3>Legendary Attack II</h3>
		<p>This Pokémon may make two attacks on its turn.</p>

		<h3>Legendary Attack III</h3>
		<p>This Pokémon has a signature move that can be used twice in one turn.</p>

		<h3>Legendary Energy I</h3>
		<p>This Pokémon can sacrifice an action to regain all PP for a single move.</p>

		<h3>Legendary Energy II</h3>
		<p>This Pokémon has double the normal PP for every move.</p>

		<h3>Legendary Nimbleness</h3>
		<p>This Pokémon may move up to its speed without provoking attacks of opportunity.</p>

		<h3>Legendary Speed (X/Day)</h3>
		<p>This Pokémon may double its movement.</p>

		<h3>Legendary Toughness</h3>
		<p>This Pokémon's health is based off the maximum hit dice roll for each of its levels.</p>

		<h3>Legendary Reflexes</h3>
		<p>This Pokémon can use two reactions in a round of combat.</p>

		<h3>Legendary Knowledge</h3>
		<p>This Pokémon can learn moves from anywhere in its move list, regardless of level.</p>

		<h3>Legendary Armor (X/Day)</h3>
		<p>As a reaction, this Pokémon may increase its AC by (1, 2, or 3) if it would cause an attack to miss.</p>
	</section>
