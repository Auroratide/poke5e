<p>In an effort of simplication, <em>any</em> final stage Pokémon can mega-evolve, requiring only one single Mega Evolution stone in the game, "Megalite".</p>
<p>When a pokemon mega-evolves, it breaks the boundaries of evolution by changing form and becoming stronger.</p>
<p>The simplified rules of Mega Evolution are as follows:</p>
<ul>
	<li>A Pokémon may only Mega Evolve at the beginning of its turn in combat, if holding Megalite. This temporary evolution lasts for up to five rounds, and can only be activated once per trainer per long rest.</li>
	<li>The Pokémon can only Mega Evolve if it is a final stage evolution at level 13 or higher.</li>
	<li>A Mega Evolved Pokémon...
		<ul>
			<li>Changes its physical form (to something mega-cool looking).</li>
			<li>Adds temporary hit points equal to double its level (that go to zero after the Mega Evolution is over).</li>
			<li>Increases its AC by 3.</li>
			<li>Doubles all of its ability score modifiers for attack, damage, and saving throw rolls/DC.</li>
		</ul>
	</li>
	<li>Megalite is a special held item that can never be stolen or switched by moves or abilities like Covet, Pickpocket, etc.</li>
</ul>
