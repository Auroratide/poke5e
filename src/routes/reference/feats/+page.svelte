	<section>
		<h2>Pokémon Feats</h2>
		<p>The following feats can be selected by your Pokémon instead of taking the Ability Score Increase given during the leveling process. New feats unique to Pokémon are printed after the list of standard feats.</p>

		<ul>
			<li>Alert</li>
			<li>Durable</li>
			<li>Observant</li>
			<li>Resilient</li>
			<li>Savage Attacker</li>
			<li>Sentinel</li>
			<li>Skill Expert (not Animal Handling)</li>
			<li>Skulker</li>
			<li>Speedy</li>
			<li>Tough</li>
		</ul>
		
		<h3>Ambidextrous</h3>
		<p>Your Pokémon can hold an addtional held item. This feat can only be taken once.</p>

		<h3>Combo Master</h3>
		<p>Your Pokémon is an expert in combining strikes against a foe. When this feature is selected, moves that have the ability to hit more than once are guaranteed to hit at least twice. (Fury Swipes, Double Slap, etc.)</p>

		<h3>Extra AC</h3>
		<p>Your Pokémon’s AC increases by 2. This bonus is included through a Pokémon’s evolutions and is separate from any AC Ups obtained through leveling. This feat can only be taken once.</p>

		<h3>Extra Move</h3>
		<p>Your Pokémon can know one more move (five total moves instead of four). You may later take this feat again to gain the ablity to know one more move (to a maximum of six moves total).</p>

		<h3>Gifted</h3>
		<p>Increase one ability score of your choice by 1, to a maximum of 22. This ability score's maximum is now 22. As a prerequisite, the Pokémon taking this feat must be at least level 10.</p>

		<h3>Natural Mount</h3>
		<p>While this Pokémon is mounted, if the creature mounting it is attacked, it can force the attack to target itself instead. Addtionally, if this Pokémon or its mounter are forced to make a Dexterity saving throw that would result in half damage on success, it can deploy additional defenses. This Pokémon can choose either itself or its mounter to take no damage if they succeed on the saving throw, and only half damage if they fail.</p>
		
		<h3>Power Sculptor</h3>
		<p>Your Pokémon is able to sculpt the power of their moves around their allies. For area of effect moves your Pokémon activates, choose 1 + MOVE allies in range to automatically succeed on their saving throw against taking damage or an effect. If the damage is halved for a successful save, they take no damage instead.</p>

		<h3>Terrain Adept</h3>
		<p>Your Pokémon is especially skilled when fighting on a specific terrain. Choose one of the following terrains when selecting this feature. Your Pokémon gains +3 to attack rolls when in this terrain. Terrains: Coastal, Swamp, Forest, Arctic, Desert, Grassland, Hill, Mountain, Underwater.</p>
		
		<h3>Tireless</h3>
		<p>Your Pokémon endures hours of rigorous training which keep it in battle longer than the average Pokémon. Gain +1 PP for every move.</p>
