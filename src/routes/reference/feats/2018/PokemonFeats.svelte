<h3>Able-Bodied</h3>
<p>Your Pokémon's body is trained to learn from and fight off ailments for long periods of time. The status "grace period" for this Pokémon is extended by two rounds.</p>

<h3>AC Up</h3>
<p>Your Pokémon’s AC increases by 1. This bonus is included through a Pokémon’s evolutions.</p>

<h3>Combo Master</h3>
<p>Your Pokémon is an expert in combining strikes against a foe. When this feat is selected, moves that have the ability to hit more than once, after the same attack roll, are guaranteed to hit at least twice. (Fury Swipes, Double Slap, Water Shuriken, etc.)</p>

<h3>Extra Move</h3>
<p>Your Pokémon can know five total moves instead of four.</p>

<h3>Hidden Ability</h3>
<p>Your Pokémon reaches inside itself to discover a new ability. It gains access to the Hidden Ability in its stat block.</p>

<h3>Melee Master</h3>
<p>Your Pokémon is a master of close combat.</p>
<ul>
	<li>Gain advantage on all attacks of opportunity.</li>
	<li>Before you make a melee attack, you can choose to take a -5 penalty to the attack roll. If the attack hits, you add +10 to the attack’s damage (Once per move).</li>
</ul>

<h3>Power Sculptor</h3>
<p>Your Pokémon is able to sculpt the power of their moves around their allies. For area of effect moves your Pokémon activates, choose 1 + MOVE allies in range to automatically succeed on their saving throw against taking damage or an effect. If the damage is halved for a successful save, they take no damage instead.</p>

<h3>Ranged Master</h3>
<p>Your Pokémon is a sharpshooter.</p>
<ul>
	<li>Your ranged attacks ignore half and three-fourths cover.</li>
	<li>Before you make a ranged attack, you can choose to take a -5 penalty to the attack roll. If the attack hits, you add +10 to the attack’s damage (Once per move).</li>
</ul>

<h3>Terrain Adept</h3>
<p>Your Pokémon is especially skilled when fighting on a specific terrain. Choose one of the following terrains when selecting this feat. Your Pokémon gains +2 to attack rolls when in this terrain. Terrains: Coastal, Swamp, Forest, Arctic, Desert, Grassland, Hill, Mountain, Underwater.</p>

<h3>Tireless</h3>
<p>Your Pokémon endures hours of rigorous training which keep it in battle longer than the average Pokémon. Gain +1 PP for every move.</p>

<h3>Wrangler</h3>
<p><small>Can be chosen for Trainers as well.</small></p>
<p>You have developed a unique set of skills that give you the ability to grapple and hold an opponent down with steady and strong hands. You gain the following benefits:</p>
<ul>
	<li>You have advantage on attack rolls against a creature you are Grappling.</li>
	<li>You can use your action to try to pin a creature already Grappled by you. To do so, make another grapple check. If you succeed, you and the creature are both Restrained until the grapple ends.</li>
	<li>When a creature attempts to use their movement to leave your reach without disengaging, you may use your reaction to attempt to Grapple the target</li>
</ul>
